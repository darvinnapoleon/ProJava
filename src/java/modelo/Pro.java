package modelo;

public class Pro {

    String nommar;
    String nompes;
    String nomsab;

    public Pro() {
    }

    public Pro(String nommar, String nompes, String nomsab) {
        this.nommar = nommar;
        this.nompes = nompes;
        this.nomsab = nomsab;
    }

    public String getNommar() {
        return nommar;
    }

    public void setNommar(String nommar) {
        this.nommar = nommar;
    }

    public String getNompes() {
        return nompes;
    }

    public void setNompes(String nompes) {
        this.nompes = nompes;
    }

    public String getNomsab() {
        return nomsab;
    }

    public void setNomsab(String nomsab) {
        this.nomsab = nomsab;
    }
    
}
